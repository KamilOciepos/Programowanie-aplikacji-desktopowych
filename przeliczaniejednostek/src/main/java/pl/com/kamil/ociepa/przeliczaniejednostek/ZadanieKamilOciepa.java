    private void ko_jMenuItemHistoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ko_jMenuItemHistoriaActionPerformed
        JFileChooser jFileChooser = new JFileChooser();
            jFileChooser.showDialog(this,"Wybierz plik");

        try {
            String text ="";
            File f = jFileChooser.getSelectedFile();
            Scanner sc = new Scanner(f);
            while(sc.hasNext()){
                text = text+sc.nextLine()+"\n";
            }
            ko_jTextAreaOdczyt.setText(text);
        }catch (FileNotFoundException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_ko_jMenuItemHistoriaActionPerformed

