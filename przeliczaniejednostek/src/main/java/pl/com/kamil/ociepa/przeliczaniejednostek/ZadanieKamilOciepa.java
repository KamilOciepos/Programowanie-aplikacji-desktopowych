    private void ko_jComboBoxPrzeliczanieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ko_jComboBoxPrzeliczanieActionPerformed
        String jednostka = (String) ko_jComboBoxPrzeliczanie.getSelectedItem();
        if(jednostka.equals("Temperatura")){
            ko_jLabelJednostki.setText("Wprowadź wartość którą chcesz przeliczyć (kelwiny): ");
        }else{
            ko_jLabelJednostki.setText("Wprowadź wartość którą chcesz przeliczyć (metry): ");
        }
        if(jednostka.equals("Temperatura")){
            ko_jLabelPrzeliczone.setText("Przeliczona wartość (stopnie Celsjusza): ");
        }else{
            ko_jLabelPrzeliczone.setText("Przeliczona wartość (kilometry): ");
        }
    }//GEN-LAST:event_ko_jComboBoxPrzeliczanieActionPerformed

    private void ko_jTextFieldJednostkaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ko_jTextFieldJednostkaActionPerformed
        String dane =("");
        String jednostka = (String) ko_jComboBoxPrzeliczanie.getSelectedItem();
        if(jednostka.equals("Temperatura")){
            String wpisane = ko_jTextFieldJednostka.getText();
            int kelwiny = Integer.parseInt(wpisane);
            double celsjusze = kelwiny-273;
            String tekst =String.valueOf(celsjusze);
            
            ko_jTextFieldPrzeliczone.setText(tekst);
            dane = "t;k= "+kelwiny+";c= "+celsjusze;
        }else{
            String wpisane = ko_jTextFieldJednostka.getText();
            double metry = Integer.parseInt(wpisane);
            double kilometry = metry/1000;
            String tekst =String.valueOf(kilometry);
            
            ko_jTextFieldPrzeliczone.setText(tekst);
            dane = "o;m= "+metry+";km= "+kilometry+"\n";
        }   
            
       
        
        
        File f = new File("przeliczanie.txt");
        try{
            FileWriter fw = new FileWriter(f,true);
            fw.write(dane+"\n");
            fw.close();
        } catch(IOException e){
            System.out.println("błąd: "+e.toString());
        }
    }//GEN-LAST:event_ko_jTextFieldJednostkaActionPerformed

